def sprite(txt):
    txt = unProxy(txt)
    try:
        return SpriteDict[txt]
    except KeyError:
        for s, i in SpriteDict.items():
            if re.search(txt, s) is not None:
                return i
        raise KeyError

def EncodeSprite(txt, issueError=True):
    return _EncodeAny("SpriteName", sprite, SpriteDict, txt, issueError)

